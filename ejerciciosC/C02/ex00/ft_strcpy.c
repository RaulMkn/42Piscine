#include <string.h>
#include <stdio.h>

char	*ft_strcpy(char *dest, const char *src)
{
	int		i;
	char	*print;

	print = dest;
	i = 0;
	while (src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (print);
}
